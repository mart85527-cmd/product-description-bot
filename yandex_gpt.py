import httpx
import logging
from config import YANDEXGPT_API_KEY, YANDEX_FOLDER_ID

API_URL = "https://llm.api.cloud.yandex.net/foundationModels/v1/completion"
logger = logging.getLogger(__name__)


async def generate_description(platform, category, characteristics, audience, advantages, keywords, tone=None):
    """Отправляет запрос в YandexGPT и возвращает готовый текст."""

    if not YANDEXGPT_API_KEY or not YANDEX_FOLDER_ID:
        raise ValueError("YANDEXGPT_API_KEY или YANDEX_FOLDER_ID не настроены в .env")

    # Инструкции по тону
    tone_instructions = {
        "Формальный": "Тон: строгий, деловой. Только факты, характеристики, параметры. Без эмоций, без восклицаний, без обращений к покупателю. Сухой технический стиль, как в спецификации.",
        "Дружелюбный": "Тон: тёплый, разговорный, с небольшой эмоцией. Обращайся к покупателю на «вы». Покажи, что товар сделает жизнь удобнее. Лёгкая эмоциональная связь.",
        "Агрессивный": "Тон: максимально продающий. Используй акционные фразы, срочность, ограниченность. «Только сегодня», «Пока не разобрали», «Цена ограничена», «Забирай сейчас». Жёсткий CTA, требующий действия прямо сейчас.",
        "Юмористический": "Тон: лёгкий, ироничный, с ненавязчивым юмором. Можно небольшую шутку, игру слов. Не переходи в грубость — держи баланс между весёлым и информативным."
    }
    tone_instruction = tone_instructions.get(tone, "") if tone and tone != "Стандартный" else ""

    # Платформенные инструкции для стиля
    platform_instructions = {
        "Wildberries": "Пиши SEO-ориентированный текст: ключевые слова естественно вплетены в описание, акцент на характеристики и выгоды для покупателя. Короткие читаемые абзацы.",
        "Ozon": "Акцент на надёжности, доставке и проверенном качестве. Текст доверительный, упоминай быструю доставку и гарантию возврата где уместно.",
        "Яндекс.Маркет": "Текст сравнительный и информативный. Подчеркни соотношение цена/качество, укажи конкретные параметры (вес, размер, материал) для фильтров.",
        "Авито": "Разговорный, тёплый стиль. Как будто пишешь соседу. Можно лёгкий юмор. Акцент на личной встрече/доставке по городу. Не похоже на магазинный шаблон.",
        "Свой сайт": "Более развёрнутый текст. История бренда или товара, эмоциональная связь с аудиторией. CTA мягче: «Оформите заказ», «Узнайте больше»."
    }
    style_instruction = platform_instructions.get(platform, "Пиши живой, конкретный текст без шаблонов.")

    tone_block = f"\n\nДОПОЛНИТЕЛЬНЫЙ ТОН:\n{tone_instruction}\n" if tone_instruction else ""

    prompt = (
        "Ты — копирайтер для e-commerce. Пишешь короткие, конкретные, живые тексты.\n\n"
        "ПРАВИЛА:\n"
        "1. НЕ используй фразы: «идеальный выбор», «отличный выбор», «идеально подходит», «заслуживает», "
        "«порадуйте», «подарите», «не только..., но и...», «обеспечит вашего питомца», «обеспечьте своего питомца», "
        "«радуйтесь», «радовать вас», «останется в отличной форме», «полноценное питание».\n"
        "2. Пиши только факты и ощущения: из чего сделано, какой вес, какой размер, на что похоже на ощупь, "
        "в каких ситуациях пригодится.\n"
        "3. Без пустых комплиментов. Без «всеми необходимыми питательными веществами».\n"
        "4. CTA — прямой приказ: «Закажите», «Купите», «Добавьте в корзину». Без слов «порадуйте», «подарите».\n\n"
        "Пример хорошего описания:\n"
        "Корм из индейки. Гранулы с кусочками мяса, без зерна. Кошки едят охотно — запах привлекает даже приверед. "
        "Через две недели заметнее блеск шерсти. Упаковка 1,5 кг хватает на месяц для кошки весом 4 кг.\n\n"
        "Пример плохого описания (НЕ ПИШИ ТАК):\n"
        "Ваш питомец заслуживает самого лучшего. Идеальный выбор. Не только вкусный, но и полезный. "
        "Порадуйте своего любимца.\n\n"
        f"Товар для описания. Площадка: {platform}.\n"
        f"Стиль для этой площадки: {style_instruction}\n\n"
        f"Категория: {category}\n"
        f"Характеристики: {characteristics}\n"
        f"Аудитория: {audience}\n"
        f"Преимущества: {advantages}\n"
        f"Ключевые слова: {keywords}\n"
        f"{tone_block}\n\n"
        "Напиши по шаблону:\n"
        "📦 Название: [короткое catchy название]\n\n"
        "📝 Описание:\n"
        "[2 абзаца с конкретными фактами и ощущениями]\n\n"
        "✨ Преимущества:\n"
        "• [конкретное преимущество 1]\n"
        "• [конкретное преимущество 2]\n"
        "• [конкретное преимущество 3]\n\n"
        "📌 Короткий текст для карточки:\n"
        "[1 предложение]\n\n"
        "🎯 CTA:\n"
        "[прямой призыв к действию]"
    )

    payload = {
        "modelUri": f"gpt://{YANDEX_FOLDER_ID}/yandexgpt/latest",
        "completionOptions": {
            "stream": False,
            "temperature": 0.4,
            "maxTokens": 1500
        },
        "messages": [
            {
                "role": "user",
                "text": prompt
            }
        ]
    }

    headers = {
        "Authorization": f"Api-Key {YANDEXGPT_API_KEY}",
        "x-folder-id": YANDEX_FOLDER_ID,
        "Content-Type": "application/json"
    }

    async with httpx.AsyncClient() as client:
        response = await client.post(API_URL, headers=headers, json=payload)
        if response.status_code != 200:
            logger.error(f"YandexGPT ответ: {response.status_code} — {response.text}")
            raise Exception(f"YandexGPT ошибка {response.status_code}: {response.text}")

        result = response.json()
        logger.info(f"YandexGPT ответ получен: {len(str(result))} символов")
        alternatives = result.get("result", {}).get("alternatives", [])
        if not alternatives:
            logger.error(f"Пустой alternatives в ответе: {result}")
            raise Exception("Пустой ответ от YandexGPT")

        return alternatives[0].get("message", {}).get("text", "Ошибка генерации")
